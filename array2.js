// array methods

// push()-- add element at last
// unshift() -- add element at first
// pop() - remove last element
// shift() -- remove first element

// push() method

let names = ["kishan","chinmay","ajay","vijay"]
q1 = names.push('ram')
console.log(q1)
console.log(names)

// unshift() method

q2 = names.unshift("kiran")
console.log(q2)
console.log(names)

// pop() method 

q3 = names.pop()
console.log(q3)
console.log(names)

// shift() methods

q4 = names.shift()
console.log(q4)
console.log(names)
